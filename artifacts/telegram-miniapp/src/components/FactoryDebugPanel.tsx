export function FactoryDebugPanel({ logs, runState, onClear, authHeaders }: Props) {
  const [open,         setOpen]         = useState(false);
  const [question,     setQuestion]     = useState("");
  const [loading,      setLoading]      = useState(false);
  const [analysis,     setAnalysis]     = useState<DebugAnalysis | null>(null);
  const [analysisErr,  setAnalysisErr]  = useState<string | null>(null);
  const [showAnalysis, setShowAnalysis] = useState(false);
  const [copied,       setCopied]       = useState(false);
  const [sessions,     setSessions]     = useState<SavedSession[]>(() => loadSessions());
  const [showHistory,  setShowHistory]  = useState(false);
  const [viewSession,  setViewSession]  = useState<SavedSession | null>(null);
  const scrollRef     = useRef<HTMLDivElement>(null);
  const prevRunState  = useRef(runState);

  // Auto-scroll live log
  useEffect(() => {
    if (open && !viewSession && scrollRef.current) {
      scrollRef.current.scrollTop = scrollRef.current.scrollHeight;
    }
  }, [logs, open, viewSession]);

  // Auto-save session when registration finishes
  useEffect(() => {
    const prev = prevRunState.current;
    prevRunState.current = runState;
    if ((runState === "done" || runState === "error") && prev === "running" && logs.length > 0) {
      const session = buildSession(logs);
      setSessions(prev => {
        const next = [session, ...prev].slice(0, MAX_SESSIONS);
        persistSessions(next);
        return next;
      });
    }
  }, [runState, logs]);

  async function analyze(targetLogs: DebugLogEntry[] = logs) {
    if (!targetLogs.length) return;
    setLoading(true);
    setAnalysisErr(null);
    setAnalysis(null);
    setShowAnalysis(true);
    try {
      const resp = await fetch("/api/v3/ai/factory-debug", {
        method: "POST",
        headers: { "Content-Type": "application/json", ...authHeaders() },
        body: JSON.stringify({ events: targetLogs, question: question.trim() || undefined }),
      });
      const json = await resp.json() as DebugAnalysis & { error?: string };
      if (!resp.ok || json.error) throw new Error(json.error ?? `HTTP ${resp.status}`);
      setAnalysis(json);
    } catch (e) {
      setAnalysisErr(String(e));
    } finally {
      setLoading(false);
    }
  }

  function copyLog() {
    const text = logs.map(e => `[${fmtTime(e.ts)}] ${e.event}: ${JSON.stringify(e.data)}`).join("\n");
    navigator.clipboard.writeText(text).then(() => {
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    }).catch(() => {});
  }

  function deleteSession(id: string) {
    setSessions(prev => {
      const next = prev.filter(s => s.id !== id);
      persistSessions(next);
      return next;
    });
    if (viewSession?.id === id) setViewSession(null);
  }

  const activeLogs = viewSession ? viewSession.logs : logs;
  const hasError   = activeLogs.some(e => e.event === "error" || (e.event === "step" && e.data.status === "error"));
  const hasRecycled = activeLogs.some(e => e.event === "sms_retry_prompt");
  const hasDcIp    = activeLogs.some(e => e.event === "preflight" && e.data.is_datacenter === true);
  const pulseColor = hasDcIp || hasError ? "#ff6b7a" : hasRecycled ? "#fb923c" : logs.length > 0 ? "#2de897" : "#64748b";

  const currentSession = buildSession(activeLogs.length > 0 ? activeLogs : logs);

  return (
    <div style={{
      margin: "14px 0 4px", borderRadius: 18, overflow: "hidden",
      border: `1px solid ${open ? "rgba(99,102,241,0.35)" : "rgba(255,255,255,0.10)"}`,
      background: open
        ? "linear-gradient(180deg,rgba(30,27,75,0.65) 0%,rgba(15,13,50,0.80) 100%)"
        : "rgba(255,255,255,0.04)",
      backdropFilter: "blur(16px)",
      transition: "border-color 0.25s,background 0.25s",
    }}>

      {/* ── Header ────────────────────────────────────────────────────────── */}
      <button onClick={() => setOpen(o => !o)} style={{
        width: "100%", display: "flex", alignItems: "center", gap: 9,
        background: "none", border: "none", cursor: "pointer",
        padding: "11px 14px", fontFamily: "inherit",
      }}>
        <span style={{
          display: "inline-flex", alignItems: "center", justifyContent: "center",
          width: 28, height: 28, borderRadius: 9,
          background: "rgba(99,102,241,0.18)", border: "1px solid rgba(99,102,241,0.35)",
          flexShrink: 0,
        }}>
          <Bug size={14} color="#a5b4fc" />
        </span>

        <span style={{ fontSize: 12.5, fontWeight: 700, color: "rgba(255,255,255,0.75)", letterSpacing: "0.03em" }}>
          Debug Log
        </span>

        {logs.length > 0 && (
          <span style={{
            display: "inline-flex", alignItems: "center", justifyContent: "center",
            minWidth: 22, height: 18, borderRadius: 9, padding: "0 6px",
            background: `${pulseColor}25`, border: `1px solid ${pulseColor}55`,
            fontSize: 10, fontWeight: 800, color: pulseColor,
          }}>{logs.length}</span>
        )}

        {hasDcIp && (
          <span style={{ fontSize: 10, fontWeight: 700, color: "#ff6b7a", background: "rgba(255,107,122,0.12)", border: "1px solid rgba(255,107,122,0.3)", borderRadius: 8, padding: "2px 7px" }}>
            DC IP!
          </span>
        )}
        {hasRecycled && !hasDcIp && (
          <span style={{ fontSize: 10, fontWeight: 700, color: "#fb923c", background: "rgba(251,146,60,0.12)", border: "1px solid rgba(251,146,60,0.3)", borderRadius: 8, padding: "2px 7px" }}>
            recycled
          </span>
        )}
        {sessions.length > 0 && (
          <span style={{ fontSize: 10, fontWeight: 700, color: "rgba(160,160,200,0.5)", background: "rgba(99,102,241,0.08)", border: "1px solid rgba(99,102,241,0.2)", borderRadius: 8, padding: "2px 7px" }}>
            {sessions.length} saved
          </span>
        )}

        <span style={{ flex: 1 }} />

        {open && logs.length > 0 && (
          <>
            <IconBtn onClick={e => { e.stopPropagation(); copyLog(); }} title="Copy raw log">
              {copied ? <Check size={10} color="#2de897" /> : <Copy size={10} />}
            </IconBtn>
            <IconBtn
              onClick={e => { e.stopPropagation(); downloadMd(viewSession ?? currentSession); }}
              title="Download .md"
              highlight
            >
              <Download size={10} />
            </IconBtn>
            <IconBtn
              onClick={e => { e.stopPropagation(); onClear(); setAnalysis(null); setShowAnalysis(false); setViewSession(null); }}
              title="Clear"
              danger
            >
              <Trash2 size={10} />
            </IconBtn>
          </>
        )}

        <span style={{ color: "rgba(255,255,255,0.35)", marginLeft: 2, display: "flex" }}>
          {open ? <ChevronUp size={15} /> : <ChevronDown size={15} />}
        </span>
      </button>

      {/* ── Body ──────────────────────────────────────────────────────────── */}
      {open && (
        <div style={{ padding: "0 16px 20px" }}>

          {/* History banner if viewing a saved session */}
          {viewSession && (
            <div style={{
              display: "flex", alignItems: "center", gap: 8,
              background: "rgba(251,146,60,0.10)", border: "1px solid rgba(251,146,60,0.25)",
              borderRadius: 10, padding: "6px 12px", marginBottom: 8,
              fontSize: 11, color: "rgba(251,200,130,0.9)",
            }}>
              <History size={12} />
              <span style={{ flex: 1 }}>Viewing: <b>{viewSession.label}</b> · {fmtDate(viewSession.savedAt)}</span>
              <button onClick={() => setViewSession(null)} style={{ background: "none", border: "none", cursor: "pointer", color: "rgba(255,255,255,0.4)", display: "flex", padding: 0 }}>
                <X size={12} />
              </button>
            </div>
          )}

          {/* Event log */}
          <EventLog logs={activeLogs} scrollRef={viewSession ? undefined : scrollRef} />

          {/* AI section */}
          <div style={{ marginTop: 16 }}>
            <Divider label="AI ANALYSIS" />

            <div style={{ display: "flex", gap: 10, alignItems: "stretch" }}>
              <input
                value={question}
                onChange={e => setQuestion(e.target.value)}
                onKeyDown={e => { if (e.key === "Enter" && !loading && activeLogs.length > 0) void analyze(activeLogs); }}
                placeholder="Запитати про сесію… (необов'язково)"
                style={{
                  flex: 1, background: "rgba(255,255,255,0.06)",
                  border: "1px solid rgba(99,102,241,0.3)",
                  borderRadius: 14, padding: "11px 14px",
                  fontSize: 13, color: "rgba(255,255,255,0.85)",
                  fontFamily: "inherit", outline: "none",
                }}
              />
              <button
                onClick={() => void analyze(activeLogs)}
                disabled={loading || activeLogs.length === 0}
                style={{
                  flexShrink: 0, display: "flex", alignItems: "center", gap: 7,
                  background: loading || activeLogs.length === 0
                    ? "rgba(99,102,241,0.08)"
                    : "linear-gradient(135deg,rgba(99,102,241,0.55),rgba(139,92,246,0.55))",
                  border: `1px solid ${loading || activeLogs.length === 0 ? "rgba(99,102,241,0.2)" : "rgba(139,92,246,0.5)"}`,
                  borderRadius: 14, padding: "11px 18px",
                  fontSize: 13, fontWeight: 700,
                  color: loading || activeLogs.length === 0 ? "rgba(160,160,255,0.35)" : "#e0e7ff",
                  cursor: loading || activeLogs.length === 0 ? "not-allowed" : "pointer",
                  fontFamily: "inherit",
                  boxShadow: loading || activeLogs.length === 0 ? "none" : "0 0 18px rgba(99,102,241,0.3)",
                  transition: "all 0.2s", whiteSpace: "nowrap",
                }}
              >
                {loading ? (
                  <><Spinner /><span>Аналіз…</span></>
                ) : (
                  <><Sparkles size={13} /><span>Аналіз AI</span></>
                )}
              </button>
            </div>

            {/* Analysis result card */}
            {showAnalysis && (
              <div style={{ marginTop: 10, background: "rgba(0,0,0,0.3)", border: "1px solid rgba(99,102,241,0.22)", borderRadius: 14, overflow: "hidden" }}>
                {loading && !analysis && (
                  <div style={{ padding: "18px 16px", display: "flex", alignItems: "center", gap: 10, color: "rgba(160,160,255,0.6)", fontSize: 12 }}>
                    <Spinner large /> Gemini аналізує сесію…
                  </div>
                )}
                {analysisErr && (
                  <div style={{ padding: "14px 16px", color: "#ff6b7a", fontSize: 12 }}>❌ {analysisErr}</div>
                )}
                {analysis && (
                  <div>
                    <div style={{ display: "flex", alignItems: "center", gap: 8, padding: "10px 14px 8px", borderBottom: "1px solid rgba(255,255,255,0.06)", background: "rgba(99,102,241,0.08)" }}>
                      <SeverityChip severity={analysis.severity} />
                      <span style={{ flex: 1 }} />
                      {analysis.engine && (
                        <span style={{
                          fontSize: 9.5, fontWeight: 700,
                          color: analysis.engine === "gemini" ? "#34d399" : "#fb923c",
                          background: analysis.engine === "gemini" ? "rgba(52,211,153,0.1)" : "rgba(251,146,60,0.1)",
                          border: `1px solid ${analysis.engine === "gemini" ? "rgba(52,211,153,0.3)" : "rgba(251,146,60,0.3)"}`,
                          borderRadius: 6, padding: "2px 7px", letterSpacing: "0.05em",
                        }}>
                          {analysis.engine.toUpperCase()}
                        </span>
                      )}
                    </div>
                    <div style={{ padding: "10px 14px 0", fontSize: 12.5, color: "rgba(255,255,255,0.9)", lineHeight: 1.6, fontWeight: 500 }}>
                      {analysis.summary}
                    </div>
                    {analysis.issues.length > 0 && (
                      <div style={{ padding: "8px 14px 0" }}>
                        {analysis.issues.map((issue, i) => (
                          <div key={i} style={{ display: "flex", gap: 7, alignItems: "flex-start", fontSize: 12, color: "rgba(255,160,160,0.9)", lineHeight: 1.6, marginBottom: 3 }}>
                            <span style={{ color: "#ff6b7a", flexShrink: 0, marginTop: 2 }}>▸</span>{issue}
                          </div>
                        ))}
                      </div>
                    )}
                    {analysis.suggestions.length > 0 && (
                      <div style={{ padding: "6px 14px 0" }}>
                        {analysis.suggestions.map((s, i) => (
                          <div key={i} style={{ display: "flex", gap: 7, alignItems: "flex-start", fontSize: 12, color: "rgba(160,240,200,0.9)", lineHeight: 1.6, marginBottom: 3 }}>
                            <span style={{ color: "#2de897", flexShrink: 0, marginTop: 2 }}>✓</span>{s}
                          </div>
                        ))}
                      </div>
                    )}
                    {analysis.analysis && analysis.analysis !== analysis.summary && (
                      <DetailedAnalysis text={analysis.analysis} />
                    )}
                  </div>
                )}
              </div>
            )}
          </div>

          {/* ── Session History ──────────────────────────────────────────── */}
          {sessions.length > 0 && (
            <div style={{ marginTop: 10 }}>
              <Divider label="SESSION HISTORY" />
              <button
                onClick={() => setShowHistory(h => !h)}
                style={{
                  width: "100%", display: "flex", alignItems: "center", gap: 8,
                  background: "rgba(255,255,255,0.04)", border: "1px solid rgba(255,255,255,0.09)",
                  borderRadius: 10, padding: "8px 12px",
                  fontSize: 11.5, fontWeight: 600, color: "rgba(200,200,220,0.7)",
                  cursor: "pointer", fontFamily: "inherit",
                }}
              >
                <History size={12} color="#a5b4fc" />
                {sessions.length} збережених сесії
                <span style={{ flex: 1 }} />
                {showHistory ? <ChevronUp size={12} /> : <ChevronDown size={12} />}
              </button>

              {showHistory && (
                <div style={{ marginTop: 6, display: "flex", flexDirection: "column", gap: 4 }}>
                  {sessions.map(s => (
                    <div key={s.id} style={{
                      display: "flex", alignItems: "center", gap: 8,
                      background: viewSession?.id === s.id ? "rgba(99,102,241,0.15)" : "rgba(255,255,255,0.035)",
                      border: `1px solid ${viewSession?.id === s.id ? "rgba(99,102,241,0.4)" : "rgba(255,255,255,0.07)"}`,
                      borderRadius: 10, padding: "7px 10px",
                    }}>
                      <span style={{ fontSize: 11, color: "rgba(255,255,255,0.75)", flex: 1, overflow: "hidden", textOverflow: "ellipsis", whiteSpace: "nowrap" }}>
                        {s.label}
                      </span>
                      <span style={{ fontSize: 9.5, color: "rgba(255,255,255,0.3)", flexShrink: 0 }}>
                        {s.eventCount}ev
                      </span>
                      {s.hasDcIp    && <span style={{ fontSize: 9, color: "#ff6b7a" }}>DC</span>}
                      {s.hasRecycled && <span style={{ fontSize: 9, color: "#fb923c" }}>♻️</span>}

                      {/* Load */}
                      <button
                        onClick={() => { setViewSession(viewSession?.id === s.id ? null : s); setShowAnalysis(false); setAnalysis(null); }}
                        style={{ ...miniBtn, color: viewSession?.id === s.id ? "#a5b4fc" : "rgba(160,160,200,0.6)" }}
                      >
                        {viewSession?.id === s.id ? "✕" : "View"}
                      </button>

                      {/* Download */}
                      <button
                        onClick={() => downloadMd(s)}
                        style={{ ...miniBtn, color: "#2de897" }}
                        title="Download .md"
                      >
                        <Download size={10} />
                      </button>

                      {/* Delete */}
                      <button
                        onClick={() => deleteSession(s.id)}
                        style={{ ...miniBtn, color: "rgba(255,107,122,0.6)" }}
                      >
                        <Trash2 size={10} />
                      </button>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}
        </div>
      )}
    </div>
  );
}

// ── Tiny reusable atoms ──────────────────────────────────────────────────────

const miniBtn: React.CSSProperties = {
  background: "none", border: "none", cursor: "pointer",
  display: "inline-flex", alignItems: "center", padding: "2px 4px",
  fontSize: 10, fontFamily: "inherit",
};

function IconBtn({
  children, onClick, title, highlight, danger,
}: {
  children: React.ReactNode;
  onClick: (e: React.MouseEvent) => void;
  title?: string;
  highlight?: boolean;
  danger?: boolean;
}) {
  return (
    <span
      role="button" tabIndex={0} title={title}
      onClick={onClick}
      onKeyDown={e => { if (e.key === "Enter") onClick(e as unknown as React.MouseEvent); }}
      style={{
        display: "inline-flex", alignItems: "center", gap: 4,
        fontSize: 10,
        color: danger ? "rgba(255,107,122,0.7)" : highlight ? "#2de897" : "rgba(255,255,255,0.4)",
        background: danger ? "rgba(255,107,122,0.07)" : highlight ? "rgba(45,232,151,0.07)" : "rgba(255,255,255,0.05)",
        borderRadius: 8, padding: "3px 8px", cursor: "pointer", transition: "color 0.2s",
      }}
    >{children}</span>
  );
}

function Spinner({ large }: { large?: boolean }) {
  const sz = large ? 14 : 12;
  return (
    <span style={{
      width: sz, height: sz, borderRadius: "50%", flexShrink: 0,
      border: "2px solid rgba(139,92,246,0.3)", borderTopColor: "#a78bfa",
      animation: "spin 0.8s linear infinite", display: "inline-block",
    }} />
  );
}

function Divider({ label }: { label: string }) {
  return (
    <div style={{ display: "flex", alignItems: "center", height: 1, background: "rgba(99,102,241,0.18)", marginBottom: 10, position: "relative" }}>
      <span style={{
        position: "absolute", left: "50%", transform: "translateX(-50%)",
        background: "rgba(30,27,75,0.9)", padding: "0 8px",
        fontSize: 9.5, color: "rgba(160,160,200,0.5)", letterSpacing: "0.08em", fontWeight: 700,
      }}>{label}</span>
    </div>
  );
}

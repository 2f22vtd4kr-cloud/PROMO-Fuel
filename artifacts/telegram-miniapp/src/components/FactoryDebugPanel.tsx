const LS_KEY = "pf_factory_debug_sessions";
const MAX_SESSIONS = 10;

// ── Persistence helpers ──────────────────────────────────────────────────────

function loadSessions(): SavedSession[] {
  try { return JSON.parse(localStorage.getItem(LS_KEY) ?? "[]") as SavedSession[]; }
  catch { return []; }
}

function persistSessions(sessions: SavedSession[]) {
  try { localStorage.setItem(LS_KEY, JSON.stringify(sessions)); } catch {}
}

function buildSession(logs: DebugLogEntry[]): SavedSession {
  const now = Date.now();
  const completedPhones = logs
    .filter(e => e.event === "complete")
    .map(e => String(e.data.phone ?? ""));
  const hasError   = logs.some(e => e.event === "error" || (e.event === "step" && e.data.status === "error"));
  const hasRecycled = logs.some(e => e.event === "sms_retry_prompt");
  const hasDcIp    = logs.some(e => e.event === "preflight" && e.data.is_datacenter === true);
  const d = new Date(now);
  const label = completedPhones.length
    ? `✓ ${completedPhones[0]}${completedPhones.length > 1 ? ` +${completedPhones.length - 1}` : ""}`
    : hasError ? "❌ Failed session"
    : hasRecycled ? "⚠️ Recycled numbers"
    : "Session";
  return {
    id: String(now),
    savedAt: now,
    label: `${label} — ${d.toLocaleTimeString()}`,
    eventCount: logs.length,
    hasError, hasRecycled, hasDcIp,
    completedPhones,
    logs,
  };
}

// ── Markdown export ──────────────────────────────────────────────────────────

function buildMarkdown(session: SavedSession): string {
  const d = new Date(session.savedAt);
  const dateStr = d.toISOString().replace("T", " ").slice(0, 19) + " UTC";

  const statusIcon = session.completedPhones.length > 0 ? "✅" : session.hasError ? "❌" : "⚠️";
  const statusText = session.completedPhones.length > 0
    ? `Completed — registered: ${session.completedPhones.join(", ")}`
    : session.hasError ? "Failed with error"
    : session.hasRecycled ? "Aborted — recycled numbers"
    : "Unknown";

  const preflight = session.logs.find(e => e.event === "preflight" && e.data.status === "done");
  const preflightLine = preflight
    ? `- **Exit IP:** ${preflight.data.exit_ip ?? "unknown"}  ${preflight.data.is_datacenter ? "⚠️ **DATACENTER IP**" : "✓ residential/mobile"}`
    : "- Preflight: not recorded";

  const stepEvents = session.logs.filter(e => e.event === "step");
  const stepLines = stepEvents.map(e => {
    const stepNames = ["", "🛒 SMSPool", "📡 Proxy", "💬 SendCode", "⏳ SMS Wait", "🤝 Sign-in", "🔒 2FA", "🪪 Profile", "💾 Save"];
    const name = stepNames[e.data.step as number] ?? `Step ${e.data.step}`;
    const msg = (e.data.message as string | undefined) ?? "";
    const codeType = msg.match(/SentCodeType\w+/)?.[0];
    return `- **${name}** → ${e.data.status}${codeType ? ` \`[${codeType}]\`` : ""}${msg && !codeType ? ` — ${msg.slice(0, 120)}` : ""}`;
  });

  const errorEvents = session.logs.filter(e => e.event === "error");
  const retryEvents = session.logs.filter(e => e.event === "sms_retry_prompt");

  const warningsSection = [...errorEvents, ...retryEvents].length > 0 ? `
## ⚠️ Errors & Warnings

${[...errorEvents, ...retryEvents].map(e => `- **${e.event}**: ${String(e.data.message ?? JSON.stringify(e.data)).slice(0, 200)}`).join("\n")}
` : "";

  const tableRows = session.logs.map(e => {
    const t = new Date(e.ts);
    const ts = `${String(t.getHours()).padStart(2,"0")}:${String(t.getMinutes()).padStart(2,"0")}:${String(t.getSeconds()).padStart(2,"0")}.${String(t.getMilliseconds()).padStart(3,"0")}`;
    const dataStr = JSON.stringify(e.data).slice(0, 200);
    return `| ${ts} | ${e.event} | ${dataStr.replace(/\|/g, "\\|")} |`;
  }).join("\n");

  return `# PROMO-Fuel Account Factory — Debug Session

> **Generated by PROMO-Fuel debug panel. Upload this file to any LLM for analysis.**

**Date:** ${dateStr}
**Result:** ${statusIcon} ${statusText}
**Total events:** ${session.eventCount}
${session.hasDcIp ? "**⚠️ DATACENTER IP DETECTED** — this causes SentCodeTypeApp on all numbers\n" : ""}${session.hasRecycled ? "**⚠️ RECYCLED NUMBERS** — SMSPool numbers already had Telegram accounts\n" : ""}
---

## Session Summary

${preflightLine}

### Steps
${stepLines.join("\n") || "- No step events recorded"}
${warningsSection}
---

## Architecture Reference (context for LLM)

> The Account Factory registers fresh Telegram accounts via SMSPool numbers + SOCKS5 proxy.
> 
> **8-step pipeline:** 1=SMSPool buy → 2=Proxy init → 3=SendCode → 4=Wait SMS → 5=Sign-in → 6=2FA → 7=Profile → 8=Save
>
> **CodeSettings rule:** ALL \`RawSendCodeRequest\` calls MUST have \`allow_app_hash=False, unknown_number=True\`
> - \`unknown_number=True\` sets flags=0x200, telling Telegram this is a NEW registration → delivers SMS
> - Without it: Telegram routes to \`SentCodeTypeApp\` (thinks it's a login attempt)
>
> **SentCodeTypeSms** ✅ = correct, continue. **SentCodeTypeApp** ❌ = recycled number or missing flag or DC IP.
>
> **Healthy sequence:** preflight(done,is_datacenter=false) → step1(done) → step2(done) → step3(done,SentCodeTypeSms) → step4(done) → step5(done) → step6(done) → step7(done) → step8(done) → complete

---

## Full Event Timeline

| Time | Event | Data |
|------|-------|------|
${tableRows}

---

## Raw JSON (for programmatic analysis)

\`\`\`json
${JSON.stringify(session.logs, null, 2)}
\`\`\`
`;
}

function downloadMd(session: SavedSession) {
  const md = buildMarkdown(session);
  const blob = new Blob([md], { type: "text/markdown;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  const d = new Date(session.savedAt);
  const stamp = `${d.getFullYear()}${String(d.getMonth()+1).padStart(2,"0")}${String(d.getDate()).padStart(2,"0")}_${String(d.getHours()).padStart(2,"0")}${String(d.getMinutes()).padStart(2,"0")}`;
  a.download = `factory_debug_${stamp}.md`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

// ── Small helpers ────────────────────────────────────────────────────────────

function fmtTime(ts: number): string {
  const d = new Date(ts);
  return `${String(d.getHours()).padStart(2,"0")}:${String(d.getMinutes()).padStart(2,"0")}:${String(d.getSeconds()).padStart(2,"0")}.${String(d.getMilliseconds()).padStart(3,"0")}`;
}

function fmtDate(ts: number): string {
  const d = new Date(ts);
  return `${d.toLocaleDateString()} ${d.toLocaleTimeString()}`;
}

function eventColor(event: string, data: Record<string, unknown>): string {
  if (event === "error") return "#ff6b7a";
  if (event === "complete") return "#2de897";
  if (event === "poll") return "#60a5fa";
  if (event === "debug") return "#06b6d4";
  if (event === "sms_retry_prompt") return "#fb923c";
  if (event === "preflight") {
    if (data.status === "error") return "#ff6b7a";
    if (data.status === "done") return "#a78bfa";
    return "#c4b5fd";
  }
  if (event === "step") {
    const s = data.status as string;
    if (s === "done") return "#2de897";
    if (s === "running") return "#f59e0b";
    if (s === "error") return "#ff6b7a";
    return "#94a3b8";
  }
  if (event.startsWith("batch")) return "#22d3ee";
  if (event.startsWith("warmup")) return "#a855f7";
  return "#94a3b8";
}

function debugLabel(data: Record<string, unknown>): string {
  const msg = String(data.message ?? "");
  try {
    const parsed = JSON.parse(msg) as Record<string, unknown>;
    const key = Object.keys(parsed)[0] ?? "DEBUG";
    return key;
  } catch {
    return msg.slice(0, 80) || "DEBUG";
  }
}

function eventLabel(entry: DebugLogEntry): string {
  const { event, data } = entry;
  if (event === "debug") return debugLabel(data);
  if (event === "step") {
    const msg = (data.message as string | undefined) ?? "";
    const names = ["","🛒 SMSPool","📡 Proxy","💬 SendCode","⏳ SMS Wait","🤝 Sign-in","🔒 2FA","🪪 Profile","💾 Save"];
    const stepName = names[data.step as number] ?? `Step ${data.step}`;
    const codeType = msg.match(/SentCodeType\w+/)?.[0];
    const short = msg.length > 60 ? msg.slice(0, 60) + "…" : msg;
    return codeType
      ? `${stepName} → ${data.status} [${codeType}]`
      : `${stepName} → ${data.status}${short ? ` — ${short}` : ""}`;
  }
  if (event === "preflight") {
    const ip = data.exit_ip ? ` [${data.exit_ip}${data.is_datacenter ? " ⚠️DC" : " ✓res"}]` : "";
    return `preflight ${data.status}${ip}${data.message ? ` — ${String(data.message).slice(0, 60)}` : ""}`;
  }
  if (event === "poll") return String(data.message ?? "…").slice(0, 80);
  if (event === "error") return `❌ ${String(data.message ?? "").slice(0, 80)}`;
  if (event === "complete") return `✓ Registered: ${data.phone ?? ""}`;
  if (event === "sms_retry_prompt") {
    const msg = String(data.message ?? "");
    return msg.includes("SentCodeTypeApp") ? `⚠️ SentCodeTypeApp — ${msg.slice(0, 60)}` : `⚠️ ${msg.slice(0, 70)}`;
  }
  if (event === "batch_start") return `Batch start — ${data.total} accounts`;
  if (event === "batch_progress") return `Progress ${data.current}/${data.total} — ✓${data.succeeded} ✗${data.failed}`;
  if (event === "batch_delay") return String(data.message ?? "").slice(0, 70);
  if (event === "batch_done") return `Batch done — ✓${data.succeeded} ✗${data.failed}/${data.total}`;
  if (event === "batch_reset") return "Batch reset";
  if (event === "warmup_prompt") return `Warmup prompt — ${data.phone}`;
  if (event === "warmup_queued") return `Warmup queued — ${data.phone}`;
  return JSON.stringify(data).slice(0, 80);
}

